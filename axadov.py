import turtle
import math

wn = turtle.Screen()
wn.bgcolor("black") 
wn.title("Rangli Spiral Illuziya")


pen = turtle.Turtle()
pen.speed(0)


colors = ["red", "yellow", "blue", "green", "purple", "orange"]


for i in range(360):
    pen.pencolor(colors[i % 6])  
    pen.width(i / 100 + 1) 
    pen.forward(math.sin(math.radians(i)) * 100)  
turtle.done()  
















































































































































































































































































































































































































































































































































































































































































































































































